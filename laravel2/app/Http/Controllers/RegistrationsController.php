    {
        $this->validate(request(), [
            'name' => 'required',
            'email' => 'required|email',
            'password' => 'required|confirmed' // to verify with password_confirmation
        ]);
        $user = User::create(request(['name', 'email', 'password']));
        auth()->login($user);
        
